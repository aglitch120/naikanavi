def validate(output):
    import json

    # taxonomy読み込み
    with open("/Users/tasuku/Desktop/iwor/docs/source-data/iwor-theme-taxonomy.json", encoding="utf-8") as f:
        taxonomy = json.load(f)

    categories = taxonomy["categories"]

    errors = []
    warnings = []

    questions = output["questions"]

    # 1. 75問あるか
    if len(questions) != 75:
        errors.append(f"問題数が75ではない: {len(questions)}")

    # 正答照合用テーブル
    expected = {
        1:"b",2:"d",3:"e",4:"d",5:"b",6:"d",7:"e",8:"d",9:"c",10:"d",
        11:"e",12:"e",13:"b",14:"e",15:"e",16:"a",17:"d",18:"a",19:"d",20:"c",
        21:"e",22:"e",23:"d",24:"e",25:"c",26:"e",27:"a",28:"e",29:"e",30:"d",
        31:"e",32:["b","e"],33:["b","e"],34:["a","b"],35:["b","d","e"],
        36:"d",37:"d",38:"e",39:"a",40:"c",41:"c",42:"a",43:"b",44:"b",45:"a",
        46:"a",47:"d",48:"c",49:"d",50:"c",51:"e",52:"d",53:"a",54:"c",55:"e",
        56:"b",57:"e",58:["a","d"],59:"a",60:"d",61:"b",62:"c",63:"e",64:"a",
        65:"b",66:"e",67:"d",68:"a",69:"e",70:"e",71:"a",72:"c",73:"a",
        74:"36",75:"27"
    }

    for q in questions:
        num = q["num"]

        # 2. 正答チェック
        exp = expected.get(num)
        got = q["answer"]
        if exp != got:
            errors.append(f"C{num:03d} 正答不一致: expected={exp}, got={got}")

        # 3. (subfield, topic) がタクソノミーに存在するか
        field = q["field"]
        subfield = q["subfield"]
        topic = q["topic"]

        if subfield not in categories.get(field, {}).get("subcategories", {}):
            # fieldレベルでsubfieldを検索
            found_sf = False
            for cat_key, cat_val in categories.items():
                if subfield in cat_val.get("subcategories", {}):
                    found_sf = True
                    if cat_key != field:
                        errors.append(f"C{num:03d} field不一致: subfield={subfield}はfield={cat_key}に属するが、field={field}と設定")
                    break
            if not found_sf:
                errors.append(f"C{num:03d} subfield={subfield}がタクソノミーに存在しない")
        else:
            # topicチェック
            topics_in_sf = categories[field]["subcategories"][subfield].get("topics", [])
            if topic not in topics_in_sf:
                warnings.append(f"C{num:03d} topic='{topic}'がsubfield={subfield}に存在しない（近似トピックで要確認）")

        # 4. field == subfield prefix
        sf_prefix = subfield.split("-")[0] if "-" in subfield else subfield
        if sf_prefix != field:
            errors.append(f"C{num:03d} field={field}とsubfield prefix={sf_prefix}が一致しない")

    print("\n=== バリデーション結果 ===")
    print(f"問題数: {len(questions)}/75")
    print(f"エラー数: {len(errors)}")
    print(f"警告数: {len(warnings)}")

    if errors:
        print("\n[ERRORS]")
        for e in errors:
            print(f"  {e}")

    if warnings:
        print("\n[WARNINGS]")
        for w in warnings:
            print(f"  {w}")

    if not errors:
        print("\nバリデーション PASSED")
    else:
        print("\nバリデーション FAILED")

if __name__ == "__main__":
    output = main()
    validate(output)
