import type { DiseaseTemplate } from './josler-templates'

/**
 * カルテrawデータ → J-OSLER病歴要約 に変換するプロンプトを生成
 */
export function generateJoslerPrompt(
  disease: string,
  template: DiseaseTemplate | undefined,
): string {
  const diseaseInfo = template
    ? `
疾患: ${template.disease}
鑑別診断: ${template.differentialDiagnosis.join(', ')}
標準治療: ${template.standardTreatment.join(', ')}
${template.guidelineRef ? `参照ガイドライン: ${template.guidelineRef}` : ''}
PubMed検索キーワード: ${template.pubmedQuery}
`
    : `疾患: ${disease || '（疾患名を記入してください）'}`

  return `# 病歴要約作成プロンプト（J-OSLER準拠）

あなたは内科専攻医として, 以下の【カルテ情報】から日本内科学会「病歴要約 作成と評価の手引き」に完全準拠した病歴要約を作成してください.

## 疾患情報
${diseaseInfo}

## 構成と文字数

以下の各セクションを, 指定の文字数以内で作成してください.

1. 【タイトル】80文字以内 — 症例内容を端的に表す
2. 【確定診断名】— #1に主病名（提出領域の疾患）, #2以降に副病名. 略語不可
3. 【主訴】25文字以内
4. 【既往歴・生活社会歴・家族歴】合計100文字以内 — 重要なもののみ. 全てを羅列しない
5. 【病歴】1000文字以内 — 主病名について時系列で記載. OPQRSTを文章に盛り込む. 陰性所見を十分に含める
6. 【主な入院時現症】350文字以内 — バイタル・身体所見. 不必要なものは減らして要領よく
7. 【主要な検査所見】1000文字以内 — 異常値・注目すべき正常値・特殊検査. ルーチンは全羅列不要（「肝機能正常」等でよい）
8. 【プロブレムリスト】300文字以内 — 重要度・緊急度の高い順. #1が主病名. 1疾患にまとめられるものは分割しない
9. 【入院後経過と考察】1500文字以内 — プロブレム番号順に【経過】と【考察】を分けて記載. 全プロブレムを漏れなく論じる
10. 【退院時処方】300文字以内 — 一般名で記載
11. 【総合考察】1000文字以内 — 診断・治療法選択の過程. 文献2-3本引用

## 文体・表現ルール（厳守）

### 基本文体
- **常体**で記述する（〜した, 〜だった, 〜された）. ですます調禁止
- **体言止め禁止**. 述語で文を閉じる（✅「心雑音は聴取しなかった」❌「心雑音なし」）
- **過去形**で記述する. **受動態**を基本とする
- 口語文禁止. 文語調で記述する

### 禁止語句
| 禁止 | 代替 |
|------|------|
| 「認める」「認めた」 | 「確認した」「検出した」「みられた」 |
| 「にて」 | 「で」「により」 |
| 体言止め（〜なし, 〜あり） | 述語で閉じる（〜なかった, 〜であった） |

### 書式
- 句読点: **「,」「.」を使用**（「、」「。」は禁止）. 後に**半角スペース**
- 英数字: **全て半角**. 数字と単位の間に半角スペース（例: CRP 15.2 mg/dL）
- 微生物名: **斜体**
- 日付: ○年○月○日で統一. 同年は「同年△月△日」

### 略語
- 略語はいきなり使わず「以下〇〇と記載」と注記
- 例外: K, Na, BUN, JCS, CT, MRI 等の一般的略語はそのまま使用可

### 薬剤名
- **一般名**で記載. 商品名→一般名に変換すること
- 不明な場合「風邪薬（詳細不明）」等と記述

### 個人情報保護
- 患者個人情報（氏名・生年月日・住所・連絡先）は**記載不可**
- 病院名は「近医」「前医」に変更
- 受診日・入院日は○年○月○日で統一

## プロブレムリスト作成の指針

- **重要度・緊急度の高い順**に並べる. 主病名が #1
- 確定診断→診断名, 未確定→症状・所見をプロブレムとする
- 「〇〇の除外」はプロブレムとして独立させない（考察に含める）
- 社会的問題（独居, 介護力不足等）も臨床上重要であれば挙げる

## 入院後経過と考察の記載方法

プロブレムリストの番号順（#1, #2...）に沿って記載. 各プロブレムに対して:

- **【経過】は事実のみ**: 「何をした・何が起きた・数値はどう推移したか」を時系列で淡々と. 診断根拠・文献引用はここに書かない
- **【考察】は理由・根拠・評価**: 診断根拠・鑑別除外根拠・治療選択理由・エビデンス・文献引用
- 主病名（#1）が最も厚く, 副病名は簡潔に

## 総合考察の記載方法

- プロブレムごとに診断・治療法選択の過程を記載
- PubMedから2-3件の文献を引用
- 引用形式: （著者. 雑誌名 年;巻:ページ）
- 最後に「本症例を通じて学んだこと」を記載

## 情報欠損時

カルテに記載がない情報は【要確認】プレースホルダーを挿入. 項目ごと省略してはならない.

### 無条件必須項目（常に記載）
**入院時現症**: 身長・体重（BMI）, 体温, 脈拍, 血圧, 心音, 呼吸音, 腹部所見, 眼瞼結膜（貧血）, 下腿浮腫
**検査所見**: CBC（Hb・WBC分画・Plt）, 生化学（TP・Alb・T-Bil・AST・ALT・BUN・Cr・Na・K・Cl）, CRP, 胸部X線

### 疾患依存必須項目
- 呼吸器・循環器・敗血症 → 呼吸数・SpO2
- 肝胆膵・溶血 → 眼球結膜（黄疸）
- 神経・脳卒中 → 意識レベル・神経学的所見
- 循環器・電解質異常 → 心電図
- 出血・血栓症 → 凝固系（PT-INR・APTT・Dダイマー）

### 出力の末尾に【要確認リスト】を記載

---

## 【カルテ情報】

（ここに匿名化済みのカルテ情報を貼り付けてください）


`
}

/**
 * 病歴要約の下書きをAIで改善するプロンプトを生成
 */
export function generateRefinePrompt(currentDraft: string): string {
  return `# 病歴要約の査読・修正プロンプト（J-OSLER準拠）

以下の病歴要約の下書きを, 日本内科学会「病歴要約 作成と評価の手引き」に基づいて査読し, 修正してください.

## 査読チェックリスト

### 1. 文体・表現
- [ ] 常体統一（ですます調なし）
- [ ] 句読点「,」「.」統一（「、」「。」なし）
- [ ] 「認める」「にて」の除去
- [ ] 体言止めなし（全て述語で閉じる）
- [ ] 個人情報匿名化（病院名→近医/前医）
- [ ] 薬剤名が全て一般名

### 2. 構成
- [ ] 主病名が提出領域の疾患であること
- [ ] プロブレムリストが重要度・緊急度順
- [ ] 【経過】と【考察】が分離されている
- [ ] プロブレム番号が一貫している

### 3. 内容
- [ ] 陰性所見が十分に含まれている
- [ ] 鑑別診断の除外根拠が明示されている
- [ ] 文献2-3本が引用形式（著者. 雑誌名 年;巻:ページ）で記載
- [ ] 患者の社会的・心理的背景への配慮

### 4. 文字数
- [ ] 各セクションが文字数上限以内

## 修正のルール

- 修正記録は不要. 修正済みの最終稿のみを全文出力
- 各セクションの文字数上限を超過しないこと
- 商品名は全て一般名に変換
- 「認める」→「確認した」, 「にて」→「で」に置換
- 体言止め→述語で閉じる

---

## 【下書き】

${currentDraft}
`
}
