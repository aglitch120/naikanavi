def build_taxonomy_lookup(taxonomy_path):
    d = json.loads(taxonomy_path.read_text(encoding="utf-8"))
    cats = d["categories"]
    all_topics = []
    for field_key, cat in cats.items():
        for subfield_key, subcat in cat["subcategories"].items():
            for topic in subcat["topics"]:
                all_topics.append({"field": field_key, "subfield": subfield_key, "topic": topic})
    topic_to_entry = {e["topic"]: e for e in all_topics}
    field_topics = {}
    for e in all_topics:
        field_topics.setdefault(e["field"], []).append(e)
    return cats, all_topics, topic_to_entry, field_topics


def find_best_topic(theme, subject, cats, field_topics):
    if theme in MANUAL_MAP:
        return MANUAL_MAP[theme]

    expected_fields = SUBJECT_TO_FIELDS.get(subject, [])
    if not expected_fields:
        return None

    candidates = []
    for f in expected_fields:
        candidates.extend(field_topics.get(f, []))

    if not candidates:
        return None

    theme_clean = re.sub(
        r'について$|の診断$|の治療$|の管理$|の原因$|の症状.*$|の検査.*$'
        r'|に対する.*$|をきたす.*$|に行う.*$|の初期治療.*$|に直ちに.*$'
        r'|にまず.*$|でみられる.*$|の対応$|の処置$|の手技$|への対応$',
        '', theme
    )
    theme_clean = theme_clean.strip()

    best = None
    best_score = 0
    for c in candidates:
        t = c["topic"]
        t_base = re.sub(r'〈.*?〉|（.*?）|\(.*?\)', '', t).strip()
        score = 0
        if t_base and t_base in theme_clean:
            score = len(t_base) * 2
        elif t_base and t_base in theme:
            score = len(t_base)
        if theme_clean and theme_clean in t:
            score = max(score, len(theme_clean))
        if score > best_score:
            best_score = score
            best = c

    if best and best_score > 0:
        return (best["field"], best["subfield"], best["topic"])

    for c in candidates:
        if "総論" in c["topic"]:
            return (c["field"], c["subfield"], c["topic"])

    if candidates:
        c = candidates[0]
        return (c["field"], c["subfield"], c["topic"])

    return None


def validate_assignment(field, subfield, topic, topic_to_entry, cats):
    errors = []
    if field not in cats:
        errors.append(f"field '{field}' not in taxonomy")
        return errors
    cat = cats[field]
    if subfield not in cat["subcategories"]:
        errors.append(f"subfield '{subfield}' not in taxonomy")
        return errors
    if topic not in topic_to_entry:
        errors.append(f"topic '{topic}' not in taxonomy")
        return errors
    entry = topic_to_entry[topic]
    if entry["subfield"] != subfield:
        errors.append(f"topic '{topic}' belongs to subfield '{entry['subfield']}', not '{subfield}'")
    if entry["field"] != field:
        errors.append(f"topic '{topic}' belongs to field '{entry['field']}', not '{field}'")
    if not subfield.startswith(field):
        errors.append(f"subfield '{subfield}' prefix doesn't match field '{field}'")
    return errors


def main():
    print("=== fix_118_topics.py ===")
    cats, all_topics, topic_to_entry, field_topics = build_taxonomy_lookup(TAXONOMY_PATH)
    print(f"Taxonomy: {len(all_topics)} topics, {len(cats)} fields")

    meta = json.loads(META_PATH.read_text(encoding="utf-8"))
    print(f"Meta: {len(meta)} entries")
    print()

    total = 0
    changed = 0
    errors = []
    not_found = []

    for block in BLOCKS:
        path = QUESTIONS_DIR / f"118{block}.json"
        data = json.loads(path.read_text(encoding="utf-8"))
        questions = data["questions"]

        for q in questions:
            qid = q["id"]
            meta_entry = meta.get(qid)
            if not meta_entry:
                print(f"  WARNING: No meta for {qid}")
                not_found.append(qid)
                continue

            theme = meta_entry["theme"]
            subject = meta_entry["subject"]

            result = find_best_topic(theme, subject, cats, field_topics)

            if result is None:
                print(f"  NOT FOUND: {qid} | {subject} | {theme}")
                not_found.append(qid)
                continue

            new_field, new_subfield, new_topic = result

            errs = validate_assignment(new_field, new_subfield, new_topic, topic_to_entry, cats)
            if errs:
                for e in errs:
                    err_msg = f"{qid} [{theme}]: {e}"
                    errors.append(err_msg)
                    print(f"  VALIDATION ERROR: {err_msg}")
                continue

            old = (q.get("field"), q.get("subfield"), q.get("topic"))
            new = (new_field, new_subfield, new_topic)
            if old != new:
                changed += 1
                if "--verbose" in sys.argv:
                    print(f"  CHANGED {qid}: {old[2]!r} → {new_topic!r}")

            q["field"] = new_field
            q["subfield"] = new_subfield
            q["topic"] = new_topic
            total += 1

        path.write_text(json.dumps(data, ensure_ascii=False, indent=2), encoding="utf-8")
        print(f"  Wrote 118{block}.json ({len(questions)} questions)")

    print()
    print("=== Results ===")
    print(f"Total processed: {total}/400")
    print(f"Changed: {changed}")
    print(f"Not found: {len(not_found)}")
    if not_found:
        print(f"  IDs: {not_found}")
    print(f"Validation errors: {len(errors)}")
    if errors:
        for e in errors:
            print(f"  ERROR: {e}")
        sys.exit(1)
    else:
        print("OK - 0 validation errors")


if __name__ == "__main__":
    main()
