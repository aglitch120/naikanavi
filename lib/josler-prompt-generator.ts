import type { DiseaseTemplate } from './josler-templates'

/**
 * カルテrawデータ → J-OSLER病歴要約 に変換するプロンプトを生成
 * プロンプト自体にはユーザーの医療データは含まれない
 */
export function generateJoslerPrompt(
  disease: string,
  template: DiseaseTemplate | undefined,
): string {
  const diseaseInfo = template
    ? `
疾患: ${template.disease}
鑑別診断: ${template.differentialDiagnosis.join(', ')}
標準治療: ${template.standardTreatment.join(', ')}
${template.guidelineRef ? `参照ガイドライン: ${template.guidelineRef}` : ''}
PubMed検索キーワード: ${template.pubmedQuery}
`
    : `疾患: ${disease || '（疾患名を記入してください）'}`

  return `# 病歴要約作成プロンプト（J-OSLER準拠）

あなたは内科専攻医として、以下の【カルテ情報】から【構成と文字数】の通りに病歴要約を作成してください。

## 疾患情報
${diseaseInfo}

## 構成と文字数

以下の各セクションを、指定の文字数以内で作成してください。

1. 【タイトル】80文字以内 — 症例内容を端的に表す
2. 【確定診断名】— #1に主病名（提出領域の疾患）、#2以降に副病名
3. 【主訴】25文字以内
4. 【既往歴・生活社会歴・家族歴】合計100文字以内 — 重要なもののみ
5. 【病歴】1000文字以内 — 主病名について時系列で記載。OPQRST。陰性所見も含める
6. 【主な入院時現症】350文字以内 — バイタル・身体所見
7. 【主要な検査所見】1000文字以内 — 異常値・注目すべき正常値・特殊検査
8. 【プロブレムリスト】300文字以内 — 重要度・緊急度の高い順。#1が主病名
9. 【入院後経過と考察】1500文字以内 — プロブレム番号順に【経過】と【考察】を分けて記載
10. 【退院時処方】300文字以内 — 一般名で記載
11. 【総合考察】1000文字以内 — 診断・治療法選択の過程。文献2-3本引用

## 記載ルール

- 文章は常体（〜した・〜だった）で記述
- 「認める」「にて」は使用禁止 → 「確認する」「検出する」「〜で」に置換
- 句読点は「,」「.」を使用（半角スペース付き）
- 英数字は全て半角、数字と単位の間にスペース
- 略語はいきなり使わず「以下〇〇と記載」と注記（一般的な略語は例外）
- 薬剤名は一般名で記載（商品名→一般名に変換すること）
- 患者個人情報（氏名・生年月日・住所・連絡先）は記載不可
- 病院名は「近医」「前医」に変更
- 微生物名は斜体

## 入院後経過と考察の記載方法

プロブレムリストの番号順（#1, #2...）に沿って記載。各プロブレムに対して:

- 【経過】は事実のみ: 「何をした・何が起きた・数値はどう推移したか」を時系列で
- 【考察】は理由・根拠: 診断根拠・鑑別除外根拠・治療選択理由・文献引用

## 総合考察の記載方法

- 診断・治療の過程を文献を引用しながら考察
- PubMedから2-3件の文献を引用（疾患名で検索して適切なものを選択）
- 引用形式: （著者. 雑誌名 年;巻:ページ）
- 最後に「本症例を通じて学んだこと」を記載

## 情報欠損時

カルテに記載がない情報は【要確認】プレースホルダーを挿入。

### 必須項目（常に記載）
- 身長・体重（BMI）、体温、脈拍、血圧、心音、呼吸音、腹部所見、眼瞼結膜、下腿浮腫
- CBC（Hb・WBC分画・Plt）、生化学（TP・Alb・T-Bil・AST・ALT・BUN・Cr・Na・K・Cl）、CRP
- 胸部X線

### 出力の末尾に【要確認リスト】を記載

---

## 【カルテ情報】

（ここに匿名化済みのカルテ情報を貼り付けてください）


`
}

/**
 * 病歴要約の下書きをAIで改善するプロンプトを生成
 */
export function generateRefinePrompt(currentDraft: string): string {
  return `# 病歴要約の査読・修正プロンプト（J-OSLER準拠）

以下の病歴要約の下書きを、日本内科学会「病歴要約 作成と評価の手引き」に基づいて査読し、修正してください。

## 査読チェックリスト

1. **基本的記載**: 文体統一（常体）/ 句読点「,」「.」/ 「認める」「にて」の除去 / 個人情報匿名化 / A4 2枚80%以上
2. **症例選択**: 主病名が提出領域の疾患であること
3. **診断プロセス**: 陰性所見 / 鑑別診断の明示と除外根拠
4. **治療法**: 薬剤一般名 / 治療選択の妥当性
5. **考察**: EBM重視 / 文献2-3本引用
6. **倫理的妥当性**: 患者の社会的・心理的背景への配慮
7. **構成の一貫性**: プロブレムリストと経過・考察の番号対応

## 修正のルール

- 修正記録は不要、修正済みの最終稿のみを全文出力
- 各セクションの文字数上限を超過しないこと
- 商品名は全て一般名に変換

---

## 【下書き】

${currentDraft}
`
}
